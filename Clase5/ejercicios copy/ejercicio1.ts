// Declarar un array de números
let numeros: number[] = [5, 10, 15, 20, 25];

// Imprimir el tercer elemento del array
console.log(numeros[2]);  // 15

// Añadir el número 30 al final del array
numeros.push(30);

// Eliminar el primer número del array
numeros.shift();

// Muestra por consola la longitud actual del array
console.log(numeros.length);  // 5
